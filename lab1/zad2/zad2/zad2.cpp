#include <iostream>
using namespace std;

int main() {
    int a = 5;
    int b = 3;

    cout << "Liczba a: " << a << endl;
    cout << "Liczba b: " << b << endl;
    cout << "Suma: " << a + b << endl;
    cout << "Różnica: " << a - b << endl;
    cout << "Iloczyn: " << a * b << endl;

    return 0;
}