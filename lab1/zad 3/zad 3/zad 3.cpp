#include <iostream>
using namespace std;

int main() {
    int i = 10;
    float f = 3.14;
    char c = 'A';
    bool b = true;

    cout << "Zmienna całkowita: " << i << endl;
    cout << "Zmienna zmiennoprzecinkowa: " << f << endl;
    cout << "Zmienna znakowa: " << c << endl;
    cout << "Zmienna logiczna: " << b << endl;

    return 0;
}